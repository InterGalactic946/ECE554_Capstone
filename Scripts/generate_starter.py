"""
This script generates starter Verilog/SystemVerilog files for RTL design and testbench development.

The workflow is intentionally optimized to reduce user typing:
- Module name is the only commonly required input.
- Both DUT and testbench starters are generated by default.
"""


# ==========================================================
# Data model
# ==========================================================
@dataclass
class Port:
    """
    Normalized port metadata container.

    Attributes:
        direction (str): Port direction from DUT perspective (input/output/inout).
        data_type (str): Signal type (typically logic).
        width (str): Optional width text without brackets (for example, 31:0).
        name (str): Port name.
        desc (str): Optional short comment for generated code readability.
    """

    direction: str
    data_type: str
    width: str
    name: str
    desc: str = ""


# ==========================================================
# Argument parsing helpers
# ==========================================================
def parse_arguments():
    """
    Parse command-line arguments for starter generation.

    Returns:
        argparse.Namespace: Parsed command-line options controlling generation
            kind and overwrite mode.
    """
    parser = argparse.ArgumentParser(description="Generate DUT/testbench starter files.")

    # Main generation knobs.
    parser.add_argument(
        "-k",
        "--kind",
        type=str,
        default="both",
        choices=["design", "testbench", "both"],
        help="Starter type to generate (default: both)",
    )

    # Parse and return all options.
    return parser.parse_args()


def normalize_kind(kind):
    """
    Normalize starter kind text into internal values.

    Args:
        kind (str): Kind text from arguments.

    Returns:
        str: One of "design", "testbench", or "both".
    """
    # Map synonyms to canonical values.
    if kind == "design":
        return "design"
    if kind == "testbench":
        return "testbench"

    # Default to generating both files.
    return "both"


