def write_file(path_obj, content):
    """
    Write generated source content to file.

    Args:
        path_obj (Path): Output file path.
        content (str): Generated source text.
        force (bool): Overwrite existing file when True.

    Returns:
        bool: True when file is written, False when skipped.
    """
    # Skip existing files unless --force is enabled.
    if path_obj.exists():
        print(f"Skipped existing file: {path_obj}")
        return False

    # Ensure destination directory exists.
    path_obj.parent.mkdir(parents=True, exist_ok=True)

    # Write output content with UTF-8 encoding.
    path_obj.write_text(content, encoding="utf-8")

    # Inform user of generated file.
    print(f"Generated: {path_obj}")
    return True


def build_outputs(module_name, kind, description, ports, tb_description=None, design_ext="sv"):
    """
    Build the list of output files and rendered content.

    Args:
        module_name (str): DUT module name.
        kind (str): Starter kind ('design', 'testbench', or 'both').
        description (str): Description used for DUT header banner.
        ports (list[Port]): Parsed port list for DUT/TB generation.
        design_ext (str): DUT extension ("sv" or "v") when DUT is generated.

    Returns:
        list[tuple[Path, str]]: Output path and rendered source text pairs.
    """
    # Accumulate generation tasks in deterministic order.
    tasks = []

    # Generate DUT output when requested.
    if kind in {"design", "both"}:
        design_file = f"{module_name}.{design_ext}"
        design_path = Path(config.DESIGNS_DIR) / design_file
        design_text = render_design_template(
            module_name=module_name,
            description=description,
            ports=ports,
            use_nettype_guard=(design_ext == "v"),
        )
        tasks.append((design_path, design_text))

    # Generate testbench output when requested.
    if kind in {"testbench", "both"}:
        tb_base = f"{module_name}_tb"
        tb_file = f"{tb_base}.sv"
        tb_path = Path(config.TESTS_DIR) / tb_file
        tb_text = render_testbench_template(
            module_name=module_name,
            description=tb_description if tb_description else f"Verifies {module_name} behavior.",
            ports=ports,
        )
        tasks.append((tb_path, tb_text))

    # Return collected generation tasks.
    return tasks


# ==========================================================
# Main workflow
# ==========================================================
def main():
    """
    Main entry point for starter generation workflow.

    Workflow:
      1. Parse command-line arguments.
      2. Resolve and setup target project directory.
      3. Resolve module name and generation kind.
      4. Parse optional compact port list.
      5. Render and write DUT/testbench starter files.

    Returns:
        None
    """
    # Parse user arguments once.
    args = parse_arguments()

    try:
        # Resolve target project and initialize config directory variables.
        selected_dir = select_project_dir()
        config.setup_directories(selected_dir)

        # Resolve starter options.
        kind = normalize_kind(args.kind)
        
        # Keep separate TB description to support DUT-derived testbench metadata.
        tb_description = ""
        design_ext = "sv"

        # Testbench-only flow: target an existing DUT and auto-derive metadata.
        if kind == "testbench":
            # Ask for DUT module name used for instantiation.
            module_name = input("DUT module name for testbench [No response is treated as MyModule]: ").strip()
            module_name = module_name if module_name else "MyModule"

            # Try to locate DUT source in designs directory.
            dut_path = find_dut_file(module_name)
            if dut_path is None:
                raise FileNotFoundError(
                    f"Could not find DUT file for '{module_name}' in {config.DESIGNS_DIR}."
                )

            # Parse DUT ports so TB signals/instantiation match the DUT interface.
            ports = parse_ports_from_dut(module_name, dut_path)

            # Derive TB description from DUT description.
            dut_description = extract_dut_description(dut_path)
            tb_description = make_testbench_description(module_name, dut_description)

            # Keep generic design description unused in testbench-only mode.
            description = "not used in testbench-only mode."
        else:
            # Prompt for module name with quick default.
            module_name = input("Module name [No response is treated as MyModule]: ").strip()
            module_name = module_name if module_name else "MyModule"

            # Ask user for design file extension when DUT generation is requested.
            design_ext = choose_design_extension()

            # Prompt for optional description with quick default.
            description_text = input(
                "Description [No response is treated as 'Implements core DUT functionality.']: "
            ).strip()
            description = (
                description_text if description_text else "Implements core DUT/Testbench functionality."
            )

            # Parse ports using one prompt for each direction.
            input_line = input(
                "Input ports (optional, comma-separated). Forms: name | name:width | type:name | type:name:width. No response is treated as no input ports.\n> "
            ).strip()
            output_line = input(
                "Output ports (optional, comma-separated). Forms: name | name:width | type:name | type:name:width. No response is treated as no output ports.\n> "
            ).strip()
            inout_line = input(
                "Inout ports (optional, comma-separated). Forms: name | name:width | type:name | type:name:width. No response is treated as no inout ports.\n> "
            ).strip()

            # Parse and merge ports while preserving prompt order.
            input_ports = parse_ports_compact(input_line, "input") if input_line else []
            output_ports = parse_ports_compact(output_line, "output") if output_line else []
            inout_ports = parse_ports_compact(inout_line, "inout") if inout_line else []
            ports = input_ports + output_ports + inout_ports

        # Build requested output files and render content.
        outputs = build_outputs(
            module_name,
            kind,
            description,
            ports,
            tb_description=tb_description,
            design_ext=design_ext,
        )

        # Write all outputs and track whether any file was actually written.
        wrote_any = False
        for output_path, content in outputs:
            wrote_any = write_file(output_path, content) or wrote_any

        # If everything was skipped (for example, existing files without --force), exit non-zero.
        if not wrote_any:
            sys.exit(1)
    except (FileNotFoundError, ValueError) as error:
        # Print user-facing error and terminate cleanly.
        print(error)
        sys.exit(1)


